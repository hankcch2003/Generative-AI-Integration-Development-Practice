from langchain_ollama import OllamaLLM

# 使用 Google 開發的輕量化模型 Gemma2 (2b 版本)，適合 CPU 環境運算
llm = OllamaLLM(model = 'gemma2:2b')

# 加入「事實檢查點」補強模型對台灣在地知識的不足，確保日期、地點與習俗正確
# 透過角色設定（專業導遊）與結構化約束，產出符合台灣習慣用語的內容
prompt = """
你是一位專業的台灣在地導遊，請使用『繁體中文』與『台灣習慣用語』。
請嚴格遵守以下事實進行介紹，嚴禁編造：
1. 主要節慶：
   - 清明節與兒童節：固定在 4/4 與 4/5 左右，習俗包括掃墓祭祖、吃潤餅。
   - 大甲媽祖遶境：核心位於「台中大甲鎮瀾宮」，是世界級宗教盛事，日期依農曆而定（約國曆3-4月）。
   - 螢火蟲季：4月至5月是全台賞螢的高峰期。
2. 氣候特徵：描述春季氣溫舒適但天氣多變，以及「清明時節雨紛紛」的降雨特性。
3. 語氣要求：親切、專業、富有導覽感，內容要 100% 真實。
4. 嚴格要求：嚴禁使用簡體字，嚴禁使用大陸用語（如：質量、優化、打印）。
請根據以上資訊，為我介紹四月的台灣。
"""

# 使用 invoke 函式將結構化的提示詞發送給 LLM 並獲取生成結果
response = llm.invoke(prompt)

# 輸出模型生成的完整回覆內容
print(response)